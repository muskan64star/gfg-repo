class Solution:
    
    #Function to find all elements in array that appear more than n/k times.
    def countOccurence(self,arr,n,k):
        #Your code here
        di = {}
        ans = 0
        count = n//k
        for i in arr:
            if i in di:
                di[i]+=1
            else:
                di[i]=1
        
        for key in di:
            if di[key]>count:
                ans+=1
        
        return ans


